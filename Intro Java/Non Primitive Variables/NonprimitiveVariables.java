public class NonprimitiveVariables {
    public static void main(String[] args) {
        /* non-primitive variables
            can store NULL but primitive variables can't
            can use methods
         */
        Integer number = null;
        System.out.println("Non-primitive Variable can store: "+number);

        String Name = "This is a chain of Characters"; // Strings can have chains of characters and are declared with double quotes
        System.out.println(Name);

    }
}
