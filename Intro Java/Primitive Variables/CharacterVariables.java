public class CharacterVariables {
    public static void main(String[] args) {
        /*
            Characters primitive Variable
                char
         */
        char Firstletter= 'A'; //Characters need to be declared with simple quote
        System.out.println(Firstletter);

        // EXTRA
        /*
            Boolean
                True or False
         */
        boolean variable= true; // or could be false
        System.out.println("This is: "+ variable);
    }
}
